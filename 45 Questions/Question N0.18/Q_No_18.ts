// Seeing the World: Think of at least five places in the world you’d like to visit.

// • Store the locations in a array. Make sure the array is not in alphabetical order.
let countriesToVisit:string[] = ['Saudi Arab','Japan', 'China', 'Turkey', 'US'];

// • Print your array in its original order.
console.log('Original Order:', countriesToVisit, '\n');

// • Print your array in alphabetical order without modifying the actual list.
console.log('Alphabetical Original Order:', countriesToVisit.sort(), '\n');

// • Show that your array is still in its original order by printing it.
console.log('Still In Original Order:', countriesToVisit, '\n');

// • Print your array in reverse alphabetical order without changing the order of the original list.
console.log('Reverse Alphabetical Original Order:', countriesToVisit.reverse(), '\n');

// • Show that your array is still in its original order by printing it again.
console.log('Still In Original Order:', countriesToVisit, '\n');

// • Reverse the order of your list. Print the array to show that its order has changed.
console.log('Original Array Reversed:', countriesToVisit.reverse(), '\n');

// • Reverse the order of your list again. Print the list to show it’s back to its original order.
console.log('Back to Original Order:', countriesToVisit.reverse(), '\n');

// • Sort your array so it’s stored in alphabetical order. Print the array to show that its order has been changed.
console.log('Sorted in Alphabetical Order:', countriesToVisit.sort(), '\n');

